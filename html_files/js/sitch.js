function testSwitch(val){
    switch(val){
        case 1:
            console.log("best grade");
            break;
        case 2:
            console.log("good grade");
            break;
        case 3:
            console.log("average grade");
            break;
        case 4:
            console.log("poor grade");
            break;
    }

}
testSwitch(4);
